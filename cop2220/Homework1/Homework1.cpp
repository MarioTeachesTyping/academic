#include <stdio.h>

// This is our main function.
// All code must be between each curly bracket '{}'.
void main()

{
	// 'printf' is a function that prints whatever string is inside.
	// '\n' is a character that creates a new line.
	// Put a semicolon at the end of each line of code to avoid a Syntax Error.
	printf("My Name is Anthony Terry.\n");
	printf("\n");
	printf("My Favorite Hobby is Playing Video Games.\n");

	// Variables that are set to initial values.
	// '=' is a get's operator essentially.
	// Left hand of '=' is a variable and right side is anything else.
	int x = (int) 10;
	int y = (int) 3;
	double z = (double) x / y;
}